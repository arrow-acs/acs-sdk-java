package com.konexios.acs.client.model;

import java.io.Serializable;

public class UserAuthenticationModel implements Serializable {
	private static final long serialVersionUID = -4441352506479306651L;

	private String username;
	private String password;

	public UserAuthenticationModel withUsername(String username) {
		setUsername(username);
		return this;
	}

	public UserAuthenticationModel withPassword(String password) {
		setPassword(password);
		return this;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((password == null) ? 0 : password.hashCode());
		result = prime * result + ((username == null) ? 0 : username.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserAuthenticationModel other = (UserAuthenticationModel) obj;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		if (username == null) {
			if (other.username != null)
				return false;
		} else if (!username.equals(other.username))
			return false;
		return true;
	}
}
